package Grass;

import java.util.Scanner;

public class Cu1022 { // 문장 1개 입력받아 그대로 출력
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        String s = sc.nextLine();
        System.out.println(s);
    }
}
// next() : 문자 또는 문자열을 공백을 기준으로 한단어 또는 한문자씩 입력받는다.
// nextLine() : 문자 또는 문장 한 라인 전체를 입력받는다. (대문자로 꼭 구별)
