package polyquiz;

public class _ByulStamp {
    public static void main(String[] args) {

        for (int i = 1; i < 6; i++) {
            for (int j = 0; j < i; j++) {
                System.out.print("*");
            }
            System.out.println();
        }

        for (int i = 6; i < 11; i++) {
            for (int j = 10; j > i; j--) {
                System.out.print("*");
            }
            System.out.println();

            // if, else 함수 써보기!

        }
    }
}